import path from 'path';
import * as vscode from 'vscode';
import * as FileSystem from 'fs';
import { spawn } from 'child_process';

export function activate(context: vscode.ExtensionContext) {
    registerCommands(context);
}

interface UMLMetadata {
    encoded: string;
    url: string;
    generatedOn: string;
    content: string;
}

async function openPlantUMLPreview(uri: vscode.Uri, context: vscode.ExtensionContext) {
    try {
        const content = FileSystem.readFileSync(uri.fsPath, 'utf-8');
        const metadata = extractUMLMetadata(content);
        
        if (metadata) {
            // Create a webview panel to display the UML
            const panel = vscode.window.createWebviewPanel(
                'plantUMLPreview',
                'PlantUML Preview',
                vscode.ViewColumn.Two,
                {
                    enableScripts: true,
                    retainContextWhenHidden: true
                }
            );

            panel.webview.html = getWebviewContent(metadata);
            
            // Handle messages from the webview
            panel.webview.onDidReceiveMessage(
                message => {
                    switch (message.command) {
                        case 'openUrl':
                            vscode.env.openExternal(vscode.Uri.parse(message.url));
                            return;
                        case 'copyUrl':
                            vscode.env.clipboard.writeText(message.url);
                            vscode.window.showInformationMessage('PlantUML URL copied to clipboard!');
                            return;
                        case 'copyEncoded':
                            vscode.env.clipboard.writeText(message.encoded);
                            vscode.window.showInformationMessage('Encoded string copied to clipboard!');
                            return;
                    }
                },
                undefined,
                context.subscriptions
            );
        } else {
            vscode.window.showErrorMessage('Could not extract UML metadata from file. Make sure the file was generated by the UML Generator.');
        }
    } catch (error) {
        vscode.window.showErrorMessage(`Error opening PlantUML preview: ${error}`);
    }
}

function extractUMLMetadata(content: string): UMLMetadata | null {
    const lines = content.split('\n');
    let encoded = '';
    let url = '';
    let generatedOn = '';
    
    // Extract metadata from comments
    for (const line of lines) {
        if (line.startsWith("' Encoded: ")) {
            encoded = line.replace("' Encoded: ", '').trim();
        } else if (line.startsWith("' URL: ")) {
            url = line.replace("' URL: ", '').trim();
        } else if (line.startsWith("' Generated on: ")) {
            generatedOn = line.replace("' Generated on: ", '').trim();
        }
    }
    
    if (encoded && url) {
        return {
            encoded,
            url,
            generatedOn,
            content: content.replace(/^'.*/gm, '').trim() // Remove comment lines for clean content
        };
    }
    
    return null;
}

